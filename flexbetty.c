/**
 * print_sum - Prints the sum of two integers
 *
 * @a: The first integer
 * @b: The second integer
 *
 * This function prints the sum of two integers to standard output.
 */
void print_sum(int a, int b)
{
	int sum = a + b;

	printf("The sum of %d and %d is %d\n", a, b, sum);
}
